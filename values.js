// matrices for model, view, and projection
function getMVPMatrices() {
    // matrices
    
    // setup model matrix;
    const modelMatrix = glMatrix.mat4.create();
    glMatrix.mat4.rotateZ(modelMatrix, modelMatrix, glMatrix.glMatrix.toRadian(0));
    
    // setup view matrix
    const viewMatrix = glMatrix.mat4.create();
    glMatrix.mat4.lookAt(viewMatrix,[0,0,0,1],[0,0,0,1],[0,0,0,0]);
    
    // setup projection matrix
    const projectionMatrix = glMatrix.mat4.create();
    // parameters to set parallel projection
    var left = -1;
    var right = 1;
    var bottom = -1;
    var top = 1;
    var near = 0;
    var far =1;
    glMatrix.mat4.ortho(projectionMatrix, left, right, bottom, top, near, far);

    return [modelMatrix, viewMatrix, projectionMatrix];
}

